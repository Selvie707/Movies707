package com.example.movies.accountmodels;

public class Datum{
    public String id;
    public String nama;
    public String username;
    public String email;
    public String password;
    public String bio;

    public String getId() {
        return id;
    }

    public String getNama() {
        return nama;
    }

    public String getUsername() {
        return username;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }

    public String getBio() {
        return bio;
    }
}
